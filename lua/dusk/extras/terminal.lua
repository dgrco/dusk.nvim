-- ─── Public API ───────────────────────────────────────────────────────────────

-- Returns a list of all supported format names.
function M.list_formats()
  local names = {}
  for k in pairs(formats) do
    table.insert(names, k)
  end
  table.sort(names)
  return names
end

-- Generate theme files.
-- variant_name: string or nil (nil = all variants)
-- format_name:  string or nil (nil = all formats)
-- output_dir:   string or nil (nil = plugin root / extras/themes)
function M.generate(variant_name, format_name, output_dir)
  local variants_mod = require("dusk.variants")
  local palette_mod  = require("dusk.palette")

  -- Determine which variants to generate
  local variant_list
  if variant_name then
    variant_list = { variant_name }
  else
    variant_list = variants_mod.list()
  end

  -- Determine which formats to generate
  local format_list
  if format_name then
    if not formats[format_name] then
      vim.notify(
        "dusk: unknown format '" .. format_name .. "'. Available: "
        .. table.concat(M.list_formats(), ", "),
        vim.log.levels.ERROR
      )
      return
    end
    format_list = { format_name }
  else
    format_list = M.list_formats()
  end

  -- Output root
  local root = output_dir or (plugin_root() .. "/extras/themes")

  local generated = {}

  for _, vname in ipairs(variant_list) do
    local c = palette_mod.get(vname)
    local t = build_term_colors(c)

    for _, fname in ipairs(format_list) do
      local fmt = formats[fname]
      local content = fmt.generate(t, vname)
      local path = root .. "/" .. fname .. "/" .. vname .. "." .. fmt.ext
      if write_file(path, content) then
        table.insert(generated, path)
      end
    end
  end

  if #generated > 0 then
    vim.notify(
      "dusk: generated " .. #generated .. " file(s):\n  "
      .. table.concat(generated, "\n  "),
      vim.log.levels.INFO
    )
  end

  return generated
end

-- Convenience: generate a single format for a single variant and return the
-- content as a string without writing to disk. Useful for testing.
function M.preview(variant_name, format_name)
  variant_name = variant_name or "dusk"
  format_name  = format_name  or "kitty"
  local fmt = formats[format_name]
  if not fmt then
    return nil, "unknown format: " .. format_name
  end
  local c = require("dusk.palette").get(variant_name)
  local t = build_term_colors(c)
  return fmt.generate(t, variant_name)
end

return M
