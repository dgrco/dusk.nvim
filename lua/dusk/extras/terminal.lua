-- lua/dusk/extras/terminal.lua
-- Terminal emulator theme generator for dusk.nvim
--
-- Generates color config files for: kitty, alacritty, wezterm, ghostty,
-- iterm2, windows-terminal, foot, tmux, and a generic ANSI table.
--
-- USAGE (from Neovim command line):
--   :lua require("dusk.extras.terminal").generate()           -- all variants, all formats
--   :lua require("dusk.extras.terminal").generate("dusk-soft") -- one variant, all formats
--   :lua require("dusk.extras.terminal").generate(nil, "kitty") -- all variants, one format
--   :lua require("dusk.extras.terminal").generate("dusk", "alacritty")
--
-- Files are written to extras/themes/<format>/ relative to the plugin root.
-- If you're using the plugin from a repo, set M.output_root below or pass
-- an output dir as a third argument.
--
-- SHELL USAGE (headless, for CI or dotfile scripts):
--   nvim --headless -c "lua require('dusk.extras.terminal').generate()" -c "qa"

local M = {}

-- ─── Helpers ──────────────────────────────────────────────────────────────────

-- Strip '#' from a hex color string.
local function hex(color)
  return color:gsub("^#", "")
end

-- Write a file, creating parent directories as needed.
local function write_file(path, content)
  -- Create parent dirs
  local dir = path:match("^(.*)/[^/]+$")
  if dir then
    vim.fn.mkdir(dir, "p")
  end
  local f = io.open(path, "w")
  if not f then
    vim.notify("dusk: could not write " .. path, vim.log.levels.ERROR)
    return false
  end
  f:write(content)
  f:close()
  return true
end

-- Find the plugin root (the directory containing this file's ancestors).
local function plugin_root()
  -- __FILE__ isn't available in Lua; use debug.getinfo as a fallback.
  local info = debug.getinfo(1, "S")
  local src = info.source:gsub("^@", "")
  -- src is .../lua/dusk/extras/terminal.lua — go up 4 levels
  return src:match("^(.*)/lua/dusk/extras/terminal%.lua$") or vim.fn.stdpath("data")
end

-- ─── Terminal color slot mapping ──────────────────────────────────────────────
-- Returns a flat table of the 16 ANSI slots + background/foreground/cursor
-- derived from the dusk palette. Same logic as init.lua _set_terminal_colors.
local function build_term_colors(c)
  return {
    -- ANSI normal (0-7)
    color0  = c.bg3,         -- black
    color1  = c.red,         -- red
    color2  = c.green,     -- green 
    color3  = c.yellow,      -- yellow
    color4  = c.blue,        -- blue
    color5  = "#b392f0",     -- magenta (terminal only)
    color6  = c.builtin,     -- cyan
    color7  = c.fg_muted,    -- white

    -- ANSI bright (8-15)
    color8  = c.fg_subtle,   -- bright black
    color9  = c.red,     -- bright red
    color10 = c.green,     -- bright green
    color11 = c.orange,      -- bright yellow
    color12 = c.blue,        -- bright blue
    color13 = "#d2a8ff",     -- bright magenta (terminal only)
    color14 = c.teal,        -- bright cyan
    color15 = c.fg,          -- bright white

    -- Semantic
    background = c.bg,
    foreground = c.fg,
    cursor     = c.fg,
    cursor_text = c.bg,
    selection_bg  = c.sel_bg,
    selection_fg  = c.sel_fg,
    url_color  = c.blue,
  }
end

