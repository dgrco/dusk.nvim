-- iterm2 ----------------------------------------------------------------------
formats.iterm2 = {
  ext = "itermcolors",
  -- iterm2 uses an Apple plist (XML) format
  generate = function(t, variant)
    -- Convert #rrggbb to r g b floats (0.0–1.0) for plist
    local function plist_color(name, hexcol)
      local r = tonumber(hexcol:sub(2,3), 16) / 255
      local g = tonumber(hexcol:sub(4,5), 16) / 255
      local b = tonumber(hexcol:sub(6,7), 16) / 255
      return table.concat({
        "\t<key>" .. name .. "</key>",
        "\t<dict>",
        "\t\t<key>Color Space</key><string>sRGB</string>",
        "\t\t<key>Red Component</key><real>" .. string.format("%.6f", r) .. "</real>",
        "\t\t<key>Green Component</key><real>" .. string.format("%.6f", g) .. "</real>",
        "\t\t<key>Blue Component</key><real>" .. string.format("%.6f", b) .. "</real>",
        "\t</dict>",
      }, "\n")
    end

    local ansi_names = {
      "Black", "Red", "Green", "Yellow", "Blue", "Magenta", "Cyan", "White",
    }
    local slots = {
      t.color0, t.color1, t.color2, t.color3, t.color4, t.color5, t.color6, t.color7,
      t.color8, t.color9, t.color10, t.color11, t.color12, t.color13, t.color14, t.color15,
    }

    local entries = {
      '<?xml version="1.0" encoding="UTF-8"?>',
      '<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN"',
      '  "http://www.apple.com/DTDs/PropertyList-1.0.dtd">',
      '<plist version="1.0">',
      "<!-- dusk.nvim — " .. variant .. " — iterm2 terminal theme -->",
      "<!-- https://github.com/yourname/dusk.nvim -->",
      "<dict>",
      plist_color("Background Color", t.background),
      plist_color("Foreground Color", t.foreground),
      plist_color("Cursor Color", t.cursor),
      plist_color("Cursor Text Color", t.cursor_text),
      plist_color("Selection Color", t.selection_bg),
      plist_color("Selected Text Color", t.selection_fg),
      plist_color("Link Color", t.url_color),
    }

    for i, color in ipairs(slots) do
      local idx = i - 1
      local prefix = idx < 8 and "Ansi " .. idx .. " Color" or "Ansi " .. idx .. " Color"
      table.insert(entries, plist_color(prefix, color))
    end

    table.insert(entries, "</dict>")
    table.insert(entries, "</plist>")
    return table.concat(entries, "\n") .. "\n"
  end,
}

